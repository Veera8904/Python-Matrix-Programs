#addition and Subtraction of Matrices

#additon
import numpy as np
 
A = np.array([[2, 4], [6, 8]])

B = np.array([[1, 3], [5, 7]])
 
print("Printing elements of first matrix")
print(A)
print("Printing elements of second matrix")
print(B)
 
print("Addition of two matrix")
print(np.add(A, B))



#Subtraction
import numpy as np
 
A = np.array([[1, 2], [3, 4]])

B = np.array([[5, 6], [7, 8]])
 
print("Printing elements of first matrix")
print(A)
print("Printing elements of second matrix")
print(B)
 
print("Subtraction of two matrix")
print(np.add(A, B))
